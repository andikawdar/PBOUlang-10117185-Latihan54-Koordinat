public class Koordinat {
    //Atribute
    int y,x;
    
    //Construction
    Koordinat (int parY, int parX){
        y = parY;
        x = parX;
    }

    //Function
    public int getY() {
        return y;
    }

    public void setY(int parY) {
        y = parY;
    }

    public int getX() {
        return x;
    }

    public void setX(int parX) {
        x = parX;
    }
    
    
}
